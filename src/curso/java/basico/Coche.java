package curso.java.basico;

public class Coche {
        String color;
        String modelo;
        String fabricante;
        double largo;
        double ancho;

        public Coche () {};

        public Coche (String color, String modelo, String fabricante, double largo, double ancho) {
                this.color = color;
                this.modelo = modelo;
                this.fabricante = fabricante;
                this.largo = largo;
                this.ancho = ancho;
        }
}
